-- Line numbers
vim.opt.nu = true
vim.opt.relativenumber = true

-- Tabbing
vim.opt.tabstop = 2
vim.opt.softtabstop = 2
vim.opt.shiftwidth = 2
vim.opt.expandtab = true

-- Indenting
vim.opt.smartindent = true

-- No line wrapping
vim.opt.wrap = false

-- Search parameters
vim.opt.hlsearch = false
vim.opt.incsearch = true

-- Better colors
vim.opt.termguicolors = true

-- Maintain 8 lines when scrolling
vim.opt.scrolloff = 8

-- Faster update time
vim.opt.updatetime = 50

-- Reserve a space in the gutter for LSP Zero
vim.opt.signcolumn = 'yes'
